//MST using prims algorithm for dense graph [Adj matrix]  =>  O(v^2) complexity
int n;
vector<vector<int>> adj; // adjacency matrix of graph
const int INF = 1000000000; // weight INF means there is no edge

struct Edge {
    int w = INF, to = -1;
};

void prim() {
    int total_weight = 0;   //totol weight of MST
    vector<bool> selected(n, false);
    //vector  ( w ,to )   (minimum weight edge/path to this index , start of path (as reversed named to))  
    vector<Edge> min_e(n);

    min_e[0].w = 0;  //first node is always has weight 0

    for (int i=0; i<n; ++i) {

        int v = -1;  // helps increment next vertex each time 
        for (int j = 0; j < n; ++j) {
            //finds minimum vertex of selected edge list of vertices
            if (!selected[j] && (v == -1 || min_e[j].w < min_e[v].w))
                v = j;
        }

        //nott MST condition ,occurs at end
        if (min_e[v].w == INF) {
            cout << "No MST!" << endl;
            exit(0);
        }

        selected[v] = true;
        total_weight += min_e[v].w;

        //prints path
        if (min_e[v].to != -1)
            cout << v << " " << min_e[v].to << endl;


        for (int to = 0; to < n; ++to) {
            //updates the values of all neighbour vertices of "v" vertex
            if (adj[v][to] < min_e[to].w)
                min_e[to] = {adj[v][to], v};
        }
    }

    cout << total_weight << endl;
}



//Uses Rank based disjoint set  for E.log V complexity  Krushkal's Algorthm sparse graph (Adj list)
vector<int> parent, rank;

void make_set(int v) {
    parent[v] = v;
    rank[v] = 0;
}

int find_set(int v) {
    if (v == parent[v])
        return v;
    return parent[v] = find_set(parent[v]);
}

void union_sets(int a, int b) {
    a = find_set(a);
    b = find_set(b);
    if (a != b) {
        if (rank[a] < rank[b])
            swap(a, b);
        parent[b] = a;
        if (rank[a] == rank[b])
            rank[a]++;
    }
}

struct Edge {
    int u, v, weight;
    bool operator<(Edge const& other) {
        return weight < other.weight;
    }
};

int n;
vector<Edge> edges;

int cost = 0;
vector<Edge> result;
parent.resize(n);
rank.resize(n);
for (int i = 0; i < n; i++)
    make_set(i);

sort(edges.begin(), edges.end());

for (Edge e : edges) {
    if (find_set(e.u) != find_set(e.v)) {
        cost += e.weight;
        result.push_back(e);
        union_sets(e.u, e.v);
    }
}





